// ─── Shared image helper ──────────────────────────────────────────────────────
// Generates a consistent vial product image per SKU label.
function vialImg(label: string, seq: string, powder = 'white'): string {
  const q = encodeURIComponent(
    `Crystal clear borosilicate glass peptide research vial labeled ${label} with ornate brass cap, `
    + `${powder} lyophilized powder at the bottom, vintage laboratory marble benchtop, `
    + `antique apothecary aesthetic, dark espresso moody background, premium product photography`
  );
  return `https://readdy.ai/api/search-image?query=${q}&width=300&height=380&seq=${seq}&orientation=portrait`;
}

